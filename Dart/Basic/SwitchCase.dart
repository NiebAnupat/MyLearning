void main(List<String> args) {
  var day = "Monday";

  switch (day) {
    case "Monday":
      print("Monday");
      break;
    case "Tuesday":
      print("Tuesday");
      break;
    case "Wednesday":
      print("Wednesday");
      break;
    case "Thursday":
      print("Thursday");
      break;
    case "Friday":
      print("Friday");
      break;
    case "Saturday":
      print("Saturday");
      break;
    case "Sunday":
      print("Sunday");
      break;
    default:
      print("Invalid day");
  }
}
