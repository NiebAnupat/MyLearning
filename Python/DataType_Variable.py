# Data Type & Variable
# ชื่อตัวแปร = ค่าที่เก็บในตัวแปร

x = 20 # integer
y = 3.99 # float
z = True # boolean
arr = [x, y, z, "Anupat"] # list
arr2 = x, y, z, "Anupat" # tuple

print("ผลลัพธ์ = "+str(x)) # แปลงค่า integer เป็น str
print(y)
print(z)
print(arr)

#เช็ก DataType F โดยจะแสดงผลชื่อ DataType ออกมา
print(type(y))
print(type("Anupat"))
print(type(z))
print(type(arr))
print(type(arr2))
